def make_question(question, options, correct_index):
    """Создаёт вопрос с перемешанными вариантами"""
    indexed = list(enumerate(options))
    random.shuffle(indexed)
    new_options = [opt for idx, opt in indexed]
    new_correct = next(i for i, (idx, opt) in enumerate(indexed) if idx == correct_index)
    return {"question": question, "options": new_options, "correct": new_correct}

