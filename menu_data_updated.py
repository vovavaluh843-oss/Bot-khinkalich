def get_all_questions():
    """Получить все вопросы из всех блюд"""
    all_questions = []
    for dish in DISHES:
        for q in dish["questions"]:
            all_questions.append({
                "dish_id": dish["id"],
                "dish_name": dish["name"],
                "category": dish["category"],
                "question": q["question"],
                "options": q["options"],
                "correct": q["correct"]
            })
    return all_questions

def get_questions_by_category(category):
    """Получить вопросы по категории"""
    questions = []
    for dish in DISHES:
        if dish["category"] == category:
            for q in dish["questions"]:
                questions.append({
                    "dish_id": dish["id"],
                    "dish_name": dish["name"],
                    "category": dish["category"],
                    "question": q["question"],
                    "options": q["options"],
                    "correct": q["correct"]
                })
    return questions

def get_dish_by_id(dish_id):
    """Получить блюдо по ID"""
    for dish in DISHES:
        if dish["id"] == dish_id:
            return dish
    return None

def get_random_question(exclude_ids=None):
    """Получить случайный вопрос"""
    all_questions = get_all_questions()
    if exclude_ids:
        all_questions = [q for q in all_questions if q["dish_id"] not in exclude_ids]
    if all_questions:
        return random.choice(all_questions)
    return None

def get_question_count():
    """Получить общее количество вопросов"""
    return len(get_all_questions())

